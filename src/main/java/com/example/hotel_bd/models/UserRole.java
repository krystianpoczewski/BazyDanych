package com.example.hotel_bd.models;

/**
 * The UserRole enum defines different levels of access within the application.
 *
 * This enum is used to categorize users into distinct roles, each with specific permissions.
 *
 * Roles:
 * - USER: Represents a standard user with limited access.
 * - ADMIN: Represents an administrative user with elevated access and permissions.
 */
public enum UserRole{
    USER,
    ADMIN
}
